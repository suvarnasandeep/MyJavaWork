package ComparatorComparable;


public class Student {
	 String name;
	 String add;
	 int rollNo;
	 
	 public Student(String name, String add, int rollNo) {
		 this.name = name;
		 this.add = add;
		 this.rollNo = rollNo;
	}
	 
	 public String getName () {
		return name;
	}
	 
	 public String getAdd() {
		return add;
	}
	 
	 public int getRollNo() {
		return rollNo;
	}
	 
}
